#include<stdio.h>
#include <stdlib.h>
int main() {
    // 1-й вираз
    int a1 = 3, a2 = 8, b1, b2, b3;
    float f1 = 1.5, f2 = 1.5, r1, r2;

    a2 -= a1;  // a2 = 8 - 3 = 5
    b1 = a1 + 2 * a2 * a2;  // b1 = 3 + 2 * 5 * 5 = 53
    b2 = !a1;  // b2 = !3 = 0
    b3 = b2++;  // b3 = b2 = 0; b2 + 1 = 1
    r1 = f1 * f1 + 2 * f2;  // r1 = 1.5 * 1.5 + 2 * 1.5 = 5.25
    r2 = (f1 != f2) ? f1 - 0.3 : f2 + 0.4;  // f1 == f2, тому r2 = 1.5 + 0.4 = 1.9

    printf("1: b1 = %d, b2 = %d, b3 = %d, r1 = %.2f, r2 = %.2f\n", b1, b2, b3, r1, r2);

    // 2-й вираз
    a1 = 2; a2 = 3;
    a2 *= 10;  // a2 = 3 * 10 = 30
    b1 = a1 * a1 * a1 + a2;  // b1 = 2 * 2 * 2 + 30 = 38
    b2 = a1 > a2;  // b2 = 2 > 30 = 0
    b3 = ++b2;  // b2 + 1 = 1, b3 = b2 = 1
    f1 = 1.5; f2 = 1.5;
    r1 = 2 * f1 - f2;  // r1 = 2 * 1.5 - 1.5 = 1.5
    r2 = (f1 == f2) ? f1 * 3 : f2 * 5;  // r2 = 1.5 * 3 = 4.5

    printf("2: b1 = %d, b2 = %d, b3 = %d, r1 = %.2f, r2 = %.2f\n", b1, b2, b3, r1, r2);

    // 3-й вираз
    a1 = 6; a2 = 20;
    a2 /= 5;  // a2 = 20 / 5 = 4
    b1 = 5 * a1 + a2 * a2;  // b1 = 5 * 6 + 4 * 4 = 30 + 16 = 46
    b2 = a1 > a2;  // b2 = 6 > 4 = 1
    b3 = --b2;  // b2 = 0, b3 = 0
    f1 = 1.5; f2 = 3.5;
    r1 = f1 + 4 * f2;  // r1 = 1.5 + 4 * 3.5 = 15.5
    r2 = (f1 >= f2) ? f1 - 2.5 : f2 + 5.2;  // r2 = 3.5 + 5.2 = 8.7

    printf("3: b1 = %d, b2 = %d, b3 = %d, r1 = %.2f, r2 = %.2f\n", b1, b2, b3, r1, r2);

    // 4-й вираз
    a1 = 3; a2 = 2;
    a2 += a1 + 5;  // a2 = 2 + 3 + 5 = 10
    b1 = a1 * a1 * a1 + 3 * a2;  // b1 = 3 * 3 * 3 + 3 * 10 = 27 + 30 = 57
    b2 = a1 < a2;  // b2 = 3 < 10 = 1
    b3 = b2--;  // b3 = 1, а потім b2 = b2 - 1 = 0
    f1 = 1.5; f2 = 2.5;
    r1 = f1 / f2 * 2;  // r1 = 1.5 / 2.5 * 2 = 1.2
    r2 = (f1 <= f2) ? f1 - 0.2 : f2 + 0.7;  // r2 = 1.5 - 0.2 = 1.3

    printf("4: b1 = %d, b2 = %d, b3 = %d, r1 = %.2f, r2 = %.2f\n", b1, b2, b3, r1, r2);

    // 5-й вираз
    a1 = 5; a2 = 13;
    a2 -= a1 + 5;  // a2 = 13 - (5 + 5) = 3
    b1 = a1 + a2 * a2;  // b1 = 5 + 3 * 3 = 5 + 9 = 14
    b2 = a1 > a2;  // b2 = 5 > 3 = 1
    b3 = b2++;  // b3 = 1, а потім b2 = b2 + 1 = 2
    f1 = 4.5; f2 = 1.8;
    r1 = f1 + f2 / 2;  // r1 = 4.5 + 1.8 / 2 = 4.5 + 0.9 = 5.4
    r2 = (f1 > f2) ? f1 + 1.2 : f2 - 1.4;  // r2 = 4.5 + 1.2 = 5.7

    printf("5: b1 = %d, b2 = %d, b3 = %d, r1 = %.2f, r2 = %.2f\n", b1, b2, b3, r1, r2);

    return 0;
    }
